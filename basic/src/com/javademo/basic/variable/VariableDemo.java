package com.javademo.basic.variable;

public class VariableDemo {
    public static void main(String[] args) {
        // 变量
        int a = 1;
        System.out.println(a);
        // 1、变量需要先声明再使用
        // 2、变量声明后，不能改变类型，不能存储其他类型的数据  强类型
        // 3、变量的有效范围 {}，同一范围内，变量不能同名
        // 4、变量定义时，可以没有初始值；使用时，必须有
        // 底层二进制存储
    }
}
